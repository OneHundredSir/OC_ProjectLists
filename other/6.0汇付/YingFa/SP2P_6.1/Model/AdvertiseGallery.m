#import "AdvertiseGallery.h"


@implementation AdvertiseGallery

- (id)initWithTitle:(NSString *)title image:(NSString *)image tag:(NSInteger)tag url:(NSString *)urlStr id:(NSString *)idStr
{
    self = [super init];
    if (self) {
        self.title = title;
        self.image = image;
        self.urlStr = urlStr;
        self.tag = tag;
        self.idStr = idStr;
    }
    return self;
}

@end
