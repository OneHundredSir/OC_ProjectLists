#import "AuctionRecordCell.h"

@implementation AuctionRecordCell

- (id)initWithStyle:(UITableViewCellStyle)style reuseIdentifier:(NSString *)reuseIdentifier
{
    self = [super initWithStyle:style reuseIdentifier:reuseIdentifier];
    if (self) {
        // Initialization code
        
        
        
        _nameLabel = [[UILabel alloc] initWithFrame:CGRectMake(0,6, MSWIDTH/3, 20)];
        _nameLabel.font = [UIFont boldSystemFontOfSize:12.0f];
        _nameLabel.textAlignment = NSTextAlignmentCenter;
        _nameLabel.textColor = [UIColor grayColor];
        [self addSubview:_nameLabel];
        
        
        _bidLabel = [[UILabel alloc] initWithFrame:CGRectMake(MSWIDTH/3,6, MSWIDTH/3, 20)];
        _bidLabel.font = [UIFont boldSystemFontOfSize:12.0f];
        _bidLabel.textAlignment = NSTextAlignmentCenter;
        _bidLabel.textColor = [UIColor grayColor];
        [self addSubview:_bidLabel];
        
        
        _timeLabel = [[UILabel alloc] initWithFrame:CGRectMake(2*MSWIDTH/3, 6, MSWIDTH/3, 20)];
        _timeLabel.font = [UIFont boldSystemFontOfSize:12.0f];
        _timeLabel.textAlignment = NSTextAlignmentCenter;
        _timeLabel.textColor = [UIColor grayColor];
        [self addSubview:_timeLabel];
        
        
    }
    return self;
}



@end
